---@module "competitest.git.config"
---Git integration configuration: default options, schema and option merging.
---Everything here is namespaced under the `git` key of the main CompetiTest config.

---A single problem, as reconstructed for the Git UI and passed to formatters.
---@class (exact) competitest.git.Problem
---@field platform string short platform code, e.g. `"cf"`, `"at"`, `"lc"`, `"cc"`, `"cs"`
---@field contest string contest identifier, e.g. `"2050"`, `"ABC412"` (may be empty)
---@field index string problem index within the contest, e.g. `"A"`, `"C"` (may be empty)
---@field id string full problem identifier, e.g. `"CF2050A"`, `"ABC412C"`, `"LC42"`, `"CSES1638"`
---@field title string human-readable problem title (may be empty when unknown)
---@field url string problem URL (may be empty when unknown)
---@field filepath string absolute path to the source file

---Metadata gathered from the Git UI and passed to formatters.
---@class (exact) competitest.git.Meta
---@field type string commit type, e.g. `"practice"`, `"solve"`
---@field difficulty string chosen difficulty, e.g. `"1700"`, `"Hard"`
---@field language string inferred/overridden language label, e.g. `"cpp23"`, `"python"`
---@field attempts integer number of attempts (defaults to 1)
---@field editorial boolean whether the solution follows an editorial
---@field contest boolean whether the problem was solved in contest mode

---Platform detection and identifier construction entry.
---@class (exact) competitest.git.Platform
---@field code string short platform code used in commit messages
---@field match fun(url: string, group: string): boolean predicate deciding whether an entry matches a task
---@field id fun(ctx: competitest.git.PlatformContext): competitest.git.PlatformResult build id/contest/index from task data

---Context handed to a platform `id` builder.
---@class (exact) competitest.git.PlatformContext
---@field url string problem URL
---@field group string task group (judge + contest)
---@field name string problem name/title
---@field taskname string short identifier derived from the URL (see `receive.lua`)

---Result returned by a platform `id` builder.
---@class (exact) competitest.git.PlatformResult
---@field id string full identifier
---@field contest string? contest identifier
---@field index string? problem index

---Selection-mode key mappings.
---@class (exact) competitest.git.mappings.selection
---@field commit keymaps enter Commit Mode
---@field push keymaps push to remote
---@field stage keymaps stage current file
---@field unstage keymaps unstage current file
---@field refresh keymaps refresh git state
---@field close keymaps close the UI

---Commit-mode key mappings.
---@class (exact) competitest.git.mappings.commit
---@field confirm keymaps confirm and commit
---@field back keymaps return to Selection Mode
---@field next_field keymaps focus next field
---@field prev_field keymaps focus previous field
---@field edit keymaps edit the focused field (opens a dropdown or prompt)
---@field cycle_next keymaps cycle the focused field to its next value
---@field cycle_prev keymaps cycle the focused field to its previous value

---Git integration configuration.
---@class (exact) competitest.Config.git
---@field enabled boolean master switch; when `false` no command is registered and no hook runs
---@field commit_on_accept false | "ui" | "auto" what to do when all testcases pass: `false` nothing, `"ui"` open the commit UI, `"auto"` commit directly
---@field default_type string default commit type selected in the UI
---@field types string[] available commit types
---@field commit_formatter nil | fun(problem: competitest.git.Problem, meta: competitest.git.Meta): string custom commit message formatter
---@field body_formatter nil | fun(problem: competitest.git.Problem, meta: competitest.git.Meta): string custom commit body formatter
---@field body boolean include a commit body generated by `body_formatter`
---@field push_after_commit boolean automatically push after a successful commit
---@field stage_testcases boolean also stage the problem's testcase files when staging the solution
---@field meta_directory string directory holding sidecars, relative to source file; supports `@/` (see `testcases_directory`)
---@field meta_file_format string sidecar file name, with CompetiTest file-format modifiers
---@field language_map table<string, string> override filetype/extension to language label mapping
---@field difficulty table<string, string[]> per-platform difficulty lists, keyed by platform code
---@field platforms competitest.git.Platform[] ordered platform detection table
---@field width number ratio between UI width and Neovim width (between 0 and 1)
---@field height number maximum ratio between UI height and Neovim height (between 0 and 1); the window shrinks to fit its content
---@field mappings { selection: competitest.git.mappings.selection, commit: competitest.git.mappings.commit }

local M = {}

---Build a range of numeric strings from `start` to `stop` (inclusive) with the given `step`.
---@param start integer
---@param stop integer
---@param step integer
---@return string[]
local function num_range(start, stop, step)
	local list = {}
	for n = start, stop, step do
		table.insert(list, tostring(n))
	end
	return list
end

