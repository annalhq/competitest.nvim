		{
			code = "cf",
			match = function(url, group)
				return string.find(url, "codeforces.com", 1, true) ~= nil or string.find(group or "", "Codeforces", 1, true) ~= nil
			end,
			---@param ctx competitest.git.PlatformContext
			id = function(ctx)
				local contest, index = string.match(ctx.url, "/(%d+)/problem/(%u+%d*)")
				if not contest then
					contest, index = string.match(ctx.url, "/problemset/problem/(%d+)/(%u+%d*)")
				end
				if not contest then
					contest, index = string.match(ctx.url, "/gym/(%d+)/problem/(%u+%d*)")
				end
				if contest and index then
					return { id = "CF" .. contest .. index, contest = contest, index = index }
				end
				return { id = "CF" .. ctx.taskname }
			end,
		},
		{
			code = "at",
			match = function(url, group)
				return string.find(url, "atcoder.jp", 1, true) ~= nil or string.find(group or "", "AtCoder", 1, true) ~= nil
			end,
			---@param ctx competitest.git.PlatformContext
			id = function(ctx)
				local slug = string.match(ctx.url, "/tasks/([%w_]+)") or ctx.taskname
				-- e.g. "abc412_c" -> contest "ABC412", index "C"
				local contest, index = string.match(slug, "^(%a+%d+)_(%w+)$")
				if contest and index then
					return { id = (contest .. index):upper(), contest = contest:upper(), index = index:upper() }
				end
				return { id = slug:upper() }
			end,
		},
		{
			code = "lc",
			match = function(url, group)
				return string.find(url, "leetcode.com", 1, true) ~= nil or string.find(group or "", "LeetCode", 1, true) ~= nil
			end,
			---@param ctx competitest.git.PlatformContext
			id = function(ctx)
				-- LeetCode names are usually "42. Trapping Rain Water"
				local number = string.match(ctx.name, "^(%d+)%.")
				if number then
					return { id = "LC" .. number, index = number }
				end
				local slug = string.match(ctx.url, "/problems/([%w-]+)") or ctx.taskname
				return { id = "LC-" .. slug }
			end,
		},
		{
			code = "cc",
			match = function(url, group)
				return string.find(url, "codechef.com", 1, true) ~= nil or string.find(group or "", "CodeChef", 1, true) ~= nil
			end,
			---@param ctx competitest.git.PlatformContext
			id = function(ctx)
				local code = string.match(ctx.url, "/problems/([%w_]+)") or ctx.taskname
				return { id = "CC" .. code }
			end,
		},
		{
			code = "cs",
			match = function(url, group)
				return string.find(url, "cses.fi", 1, true) ~= nil or string.find(group or "", "CSES", 1, true) ~= nil
			end,
			---@param ctx competitest.git.PlatformContext
			id = function(ctx)
				local number = string.match(ctx.url, "/task/(%d+)") or ctx.taskname
				return { id = "CSES" .. number, contest = number }
			end,
		},
	},
