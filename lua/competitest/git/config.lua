---Keys whose list values must be replaced (not index-merged) when the user overrides them.
---`vim.tbl_deep_extend` merges array-like tables by key, which corrupts ordered lists;
---these are assigned verbatim from the user options instead.
---@param merged competitest.Config.git the deep-merged git config (mutated in place)
---@param user competitest.Config.git? the user-provided git options
function M.fix_list_options(merged, user)
	if not user then
		return
	end
	if user.types then
		merged.types = user.types
	end
	if user.platforms then
		merged.platforms = user.platforms
	end
	if user.difficulty then
		for platform, list in pairs(user.difficulty) do
			merged.difficulty[platform] = list
		end
	end
end

return M
